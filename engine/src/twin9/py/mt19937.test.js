describe("PyRandom — parité bit-à-bit avec random.Random CPython", () => {
  for (const entry of VECTORS.seeds) {
    const { seed } = entry;

    describe(`seed ${seed}`, () => {
      it("random() : 10 premiers tirages (genrand_res53)", () => {
        const r = new PyRandom(seed);
        for (const expected of entry.random10) {
          expect(r.random()).toBe(expected);
        }
      });

      it("getrandbits(k) pour k = 1..53", () => {
        const r = new PyRandom(seed);
        const { ks, vals } = entry.getrandbits;
        ks.forEach((k, i) => {
          expect(r.getrandbits(k)).toBe(vals[i]);
        });
      });

      it("randint(a, b) via _randbelow (rejet par getrandbits)", () => {
        const r = new PyRandom(seed);
        const { pairs, vals } = entry.randint;
        let i = 0;
        for (const [a, b] of pairs) {
          expect(r.randint(a, b)).toBe(vals[i++]);
          expect(r.randint(a, b)).toBe(vals[i++]);
        }
      });

      it("choice(range(7)) : 10 tirages", () => {
        const r = new PyRandom(seed);
        const pop = [0, 1, 2, 3, 4, 5, 6];
        for (const expected of entry.choice7x10) {
          expect(r.choice(pop)).toBe(expected);
        }
      });

      it("shuffle : listes de 10 puis 25 (état partagé)", () => {
        const r = new PyRandom(seed);
        const x10 = Array.from({ length: 10 }, (_, i) => i);
        r.shuffle(x10);
        expect(x10).toEqual(entry.shuffle.x10);
        const x25 = Array.from({ length: 25 }, (_, i) => i);
        r.shuffle(x25);
        expect(x25).toEqual(entry.shuffle.x25_apres_x10);
      });

      it("sample : branches pool (n ≤ setsize) ET set (n > setsize)", () => {
        const r = new PyRandom(seed);
        for (const { n, k, out } of entry.samples) {
          const pop = Array.from({ length: n }, (_, i) => i);
          expect(r.sample(pop, k)).toEqual(out);
        }
      });

      it("séquence mêlée : random/randint/choice/sample/getrandbits/shuffle", () => {
        const r = new PyRandom(seed);
        const [v0, v1, v2, v3, v4, v5, v6, v7] = entry.mixed;
        expect(r.random()).toBe(v0);
        expect(r.randint(0, 100)).toBe(v1);
        expect(r.choice([0, 1, 2, 3, 4])).toBe(v2);
        expect(r.sample(Array.from({ length: 12 }, (_, i) => i), 4)).toEqual(v3);
        expect(r.random()).toBe(v4);
        expect(r.getrandbits(31)).toBe(v5);
        const y = [0, 1, 2, 3, 4, 5];
        r.shuffle(y);
        expect(y).toEqual(v6);
        expect(r.random()).toBe(v7);
      });
    });
  }

  it("seed négatif = valeur absolue (comme CPython)", () => {
    const a = new PyRandom(-42);
    const b = new PyRandom(42);
    expect(a.random()).toBe(b.random());
  });

  it("re-seed réinitialise l'état", () => {
    const r = new PyRandom(42);
    const first = r.random();
    r.random();
    r.seed(42);
    expect(r.random()).toBe(first);
  });
});
