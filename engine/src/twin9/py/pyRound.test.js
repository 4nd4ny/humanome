describe("pyRound — round() Python half-to-even sur le double binaire", () => {
  for (const [x, n, expected] of VECTORS.ndigits) {
    it(`round(${x}, ${n}) === ${expected}`, () => {
      expect(Object.is(pyRound(x, n), expected)).toBe(true);
    });
  }

  it("cas emblématique : round(2.675, 2) === 2.67 (2.675 binaire < 2.675)", () => {
    expect(pyRound(2.675, 2)).toBe(2.67);
  });

  for (const [x, expected] of VECTORS.entier) {
    it(`round(${x}) === ${expected} (moitiés vers le pair)`, () => {
      expect(Object.is(pyRound(x), expected)).toBe(true);
    });
  }
});

describe("pyDivmod / pyMod — signe du diviseur (sémantique Python)", () => {
  for (const [a, b, dm, mod] of VECTORS.divmod) {
    it(`divmod(${a}, ${b}) === [${dm}] ; ${a} % ${b} === ${mod}`, () => {
      expect(pyDivmod(a, b)).toEqual(dm);
      expect(Object.is(pyMod(a, b), mod)).toBe(true);
    });
  }
});

describe("formatFixed — arrondi correct de %.Nf (voir pyStr.test.js pour % complet)", () => {
  it("ties half-even sur la valeur exacte du double", () => {
    expect(formatFixed(0.25, 1)).toBe("0.2");
    expect(formatFixed(0.125, 2)).toBe("0.12");
    expect(formatFixed(2.675, 2)).toBe("2.67");
    expect(formatFixed(2.5, 0)).toBe("2");
    expect(formatFixed(-0, 1)).toBe("-0.0");
    expect(formatFixed(NaN, 2)).toBe("nan");
    expect(formatFixed(Infinity, 2)).toBe("inf");
    expect(formatFixed(-Infinity, 2)).toBe("-inf");
  });
});
